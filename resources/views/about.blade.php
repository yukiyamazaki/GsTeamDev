      <p>スクールハブとは、学校の先生を対象とした、授業で使用する資料を先生同士で共有するためのコミュニティプラットフォームです。</p>

      <p>学校の授業で使用する資料を先生がお互いに投稿しあい、みんなで共有して利用することで、より良い授業を行うことを目指しています。</p>


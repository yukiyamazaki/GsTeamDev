    <div class="future">

      <h2 class="about_h2">実現したい未来</h2>

      <p>『学校で学んだことが、子供たちの「生きる力」となって、明日に、そしてその先の人生につながってほしい。これからの社会が、どんなに変化して予測困難な時代になっても、自ら課題を見付け、自ら学び、自ら考え、判断して行動し、それぞれに思い描く幸せを実現してほしい。そして、明るい未来を、共に創っていきたい。』</p>

      <p>2020年4月から始まった新しい学習指導要領には、このような願いが込められています。今まで大切にされてきた『子供たちに「生きる力」を育む』という教育の目標はこれからも変わることはありません。しかし、社会の状況が大きく変化する時代、『生きる力』の定義も大きく変わり、それに伴い教育も大きく変わる必要があります。</p>

      <p>「生きる力」を育むためには、「学ぶ内容」も変化しますが、「学ぶ方法」も変化する必要があります。そのためには、学校の授業が時代に合わせて変化する必要があります。</p>

      <p>いま、学校教育全体が、変化の波に直面しています。一人ひとりの先生が、自分ひとりでこの変化に対応するのには、限界があります。しかし、「教育を変えたい」という志を持った人々が集まれば、時代の変化に対応することが可能だと私たちは信じています。</p>

      <p>得意分野がある先生が授業の資料などをアップロードし、その資料をそのまま、あるいはカスタマイズして他の先生が授業で使う。一つの資料をみんなでアップデートし、より良い資料に仕上げる。教師以外の専門家や研究者、企業が、総合学習などで使える教材や資料をアップロードする。それをすべての先生が利用できるようにする。こういった仕組みを作ることで、学校の授業の質を全体的に向上させることが目標です。</p>

      <p>学校の先生は、授業の準備に長い時間を割いています。スクールハブを通して授業の準備時間を短縮できることは大きなメリットの一つです。しかし、スクールハブで一番実現したいことは、授業の準備時間短縮の先にあります。</p>

      <p>先生が生徒と向き合う時間やより良い教育を極めるための自己研鑽の時間を増やしてほしい。そして、生徒の学ぶ意欲を促進する授業へ変化を促したいと考えています。</p>

      <p>スクールハブは、様々な経験や専門性を持った先生の新たな視点、考え方に「出会う」場です。スクールハブを通して、これからの時代の新しい授業、楽しい授業をみんなで作っていきたい。</p>

      <p>私たちは、教師の皆さん、教育業界の皆さん、教育業界以外の皆さんととともに新しい学校教育のカタチを作っていきたい、教育を変え、世の中を変えていきたいいきたいと考えています。

      <h3 class="about_h3">スクールハブは、変化への対応を後押し、<br>教師のみなさんと一緒に新しい教育を作っていきます。</h3>


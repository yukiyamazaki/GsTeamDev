      <p>（仮）スクールハブは、先生方が持つ授業の資料をお互いに共有することで、新たなアイデアを入手し、授業の質を高め、授業準備を効率化することを目的としています。本サービスは、デジタルハリウッド大学が運営する、起業家・エンジニア養成学校「G's Academy Tokyo」の（仮）Team Yamazakiにより作成されました。</p>


      <h2 class="about_h2">解決したい課題</h2>

      <p>現在、世の中は非常に速いスピードで変化しています。時代の変化に合わせて教育も変化する必要がありますが、多くの教育現場では授業内容が時代の変化に対応できていません。</p>

      <p>また、先生が黒板に板書し、それを生徒がノートに書き写すスタイルは、戦後数十年変わっていません。この授業スタイルは長所もたくさんありますが、板書する時間と子供に背を向けている時間が長いため、生徒が飽きてしまうといったデメリットもあります。</p>

      <p>パワーポイントなどの電子化された授業資料があれば、プロジェクターで投影することで、板書と書き写しの時間を節約し、生徒が考えたりディスカッションをする時間に充てることもできます。さらに資料の修正や共有も楽になります。</p>

      <p>全国で同じ教科書を使用している学校はたくさんあります。同じ学年の同じ教科であれば、授業の内容は基本的に同じのはずですが、多くの先生方は、すべて自分で、一から授業資料を作成することが多く、授業の準備に非常に多くの時間がかかり、長時間労働につながっています。</p>

      <p>民間企業では、社内で資料を共有し、必要に応じて各自でカスタマイズすることが一般的です。時代の変化に対応するために、教師自身が新たな知識を得る必要がありますが、勉強する時間がなく、十分に自己研鑽できていないことが多くなっています。</p>

      <p>全国各地の学校で、非常によい授業を行っている先生がたくさんいますが、そのような素晴らしい授業でどのような教材を使用しているかの情報を全国的に共有し、再利用する仕組みはありませんでした。これらの課題を解決する仕組みを作り、日本の教育をより良いものにしたいと考えています。</p>

